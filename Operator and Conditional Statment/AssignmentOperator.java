public class AssignmentOperator {
    public static void main(String args[])
    {
        int a=7;
        System.out.println(a);
        a = a +5;
        System.out.println(a);
        a += 5;
        System.out.println(a);
        a -= 5;
        System.out.println(a);
        a *= 5;
        System.out.println(a);
        a /= 5;
        System.out.println(a);
        a %= 5;
        System.out.println(a);
    }
}
