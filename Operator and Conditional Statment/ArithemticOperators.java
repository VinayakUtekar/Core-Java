class ArithemticOperators{
    public static void main(String args[])
    {
        int a=7, b=4;
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a);
        System.out.println(b);
    }
}
