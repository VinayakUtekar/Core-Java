class StringOperations{
    public static void main(String args[]){
        String s1 = "Kamal";
        String s2 = "Classes";
        System.out.println(s1);
        System.out.println(s2);
        
        String s3 = s1 + s2;
        System.out.println(s3);
        
        s3 += "rocks";
        System.out.println(s3);
    }
}